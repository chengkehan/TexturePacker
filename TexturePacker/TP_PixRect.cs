using System;
using System.Collections.Generic;

public class TP_PixRect
{
    public int x;                           
    public int y;                           
    public int width;                       
    public int height;                      

    public TP_PixRect() 
    {
            
    }

    public TP_PixRect(int x, int y, int w, int h)
    {
        this.x = x;
        this.y = y;
        this.width = w;
        this.height = h;
    }
}
